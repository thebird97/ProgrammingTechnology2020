/**
 * The {@code Direction} enum is used to determine which way the Snake is moving.
 * @author Brendan Jones
 *
 */
public enum Direction {

	/**
	 * Moving North (Up).
	 */
	North,
	
	/**
	 * Moving East (Right).
	 */
	East,
	
	/**
	 * Moving South (Down).
	 */
	South,
	
	/**
	 * Moving West (Left).
	 */
	West
	
}